def format_log_source_priorities() -> str:
    """返回 Markdown 表格，便于 CLI、README 或工单系统复用。"""
    lines = [
        "HVV / 重保应急日志源采集矩阵",
        "",
        "优先级说明：P0=第一轮必采；P1=第二轮补全攻击链；P2=溯源、影响评估和处置支撑。",
        "",
        "| 优先级 | 类别 | 类型 | 日志源 | 重要性 | 必备字段 | 研判重点 | 建议时间窗 |",
        "| --- | --- | --- | --- | --: | --- | --- | --- |",
    ]
    lines.extend(
        (
            f"| {item.priority} | {item.category} | {item.source_type} | "
            f"{item.name} | {item.importance} | {item.required_fields} | "
            f"{item.focus} | {item.collection_window} |"
        )
        for item in LOG_SOURCE_PRIORITIES
    )
    return "\n".join(lines)
